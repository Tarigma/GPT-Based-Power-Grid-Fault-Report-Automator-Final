package com.example.BlackBox.model;


public class FaultReport {
    private String incidentDescription;
    private String causeIdentification;
    private String equipmentInvolved;
    private String protectionSystems;
    private String sequenceOfEvents;
    private String faultDurationAndImpact;
    private String actionsTaken;
    private String analysisAndRecommendations;
    private String dataAndMeasurements;

    public String getIncidentDescription() {
        return incidentDescription;
    }

    public void setIncidentDescription(String incidentDescription) {
        this.incidentDescription = incidentDescription;
    }

    public String getCauseIdentification() {
        return causeIdentification;
    }

    public void setCauseIdentification(String causeIdentification) {
        this.causeIdentification = causeIdentification;
    }

    public String getEquipmentInvolved() {
        return equipmentInvolved;
    }

    public void setEquipmentInvolved(String equipmentInvolved) {
        this.equipmentInvolved = equipmentInvolved;
    }

    public String getProtectionSystems() {
        return protectionSystems;
    }

    public void setProtectionSystems(String protectionSystems) {
        this.protectionSystems = protectionSystems;
    }

    public String getSequenceOfEvents() {
        return sequenceOfEvents;
    }

    public void setSequenceOfEvents(String sequenceOfEvents) {
        this.sequenceOfEvents = sequenceOfEvents;
    }

    public String getFaultDurationAndImpact() {
        return faultDurationAndImpact;
    }

    public void setFaultDurationAndImpact(String faultDurationAndImpact) {
        this.faultDurationAndImpact = faultDurationAndImpact;
    }

    public String getActionsTaken() {
        return actionsTaken;
    }

    public void setActionsTaken(String actionsTaken) {
        this.actionsTaken = actionsTaken;
    }

    public String getAnalysisAndRecommendations() {
        return analysisAndRecommendations;
    }

    public void setAnalysisAndRecommendations(String analysisAndRecommendations) {
        this.analysisAndRecommendations = analysisAndRecommendations;
    }

    public String getDataAndMeasurements() {
        return dataAndMeasurements;
    }

    public void setDataAndMeasurements(String dataAndMeasurements) {
        this.dataAndMeasurements = dataAndMeasurements;
    }




}